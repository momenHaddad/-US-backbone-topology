void astar(int graph[V][V], int bandwidth[V][V], int delay[V][V], double reliability[V][V], int src, int dest) {
    int dist[V];
    int bw[V];
    int delayArr[V];
    int parent[V];
    bool sptSet[V];
    double reliabilityy[V];
    float reliabilityThreshold = 0.70;

    for (int i = 0; i < V; i++) {
        dist[i] = INT_MAX;
        bw[i] = 0;
        delayArr[i] = 0;
        parent[i] = -1;
        sptSet[i] = false;
        reliabilityy[i]= 0;
    }

    dist[src] = 0;
    bw[src] = 5;

    Node start = {src, 0};  
    Node goal = {dest, 0};  

    Node priorityQueue[V * V];  
    int pqSize = 0;

    priorityQueue[pqSize++] = start;

    while (pqSize > 0) {
        Node current = priorityQueue[0];
        int u = current.vertex;

        for (int i = 0; i < pqSize - 1; i++) {
            priorityQueue[i] = priorityQueue[i + 1];
        }
        pqSize--;

        sptSet[u] = true;

        for (int v = 0; v < V; v++) {
            if (!sptSet[v] && graph[u][v] && dist[u] != INT_MAX &&
                dist[u] + graph[u][v] < dist[v] && delayArr[u] + delay[u][v] < 40) {

                dist[v] = dist[u] + graph[u][v];
                bw[v] = bandwidth[u][v];
                delayArr[v] = delayArr[u] + delay[u][v];
                parent[v] = u;
                reliabilityy[v] = reliabilityy[u] + reliability[u][v];

                int totalCost = bw[v] * delayArr[v];

                Node neighbor = {v, totalCost + heuristic(v, goal.vertex)};
                priorityQueue[pqSize++] = neighbor;
            }
        }
    }

    printSolution(parent, dist, bw, delayArr, reliabilityy);
}


