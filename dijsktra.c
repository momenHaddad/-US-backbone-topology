int minDistance(int dist[], bool sptSet[]) {
    int min = INT_MAX, min_index;
    for (int v = 0; v < V; v++) {
        if (sptSet[v] == false && dist[v] <= min) {
            min = dist[v];
            min_index = v;
        }
    }
    return min_index;
}

void printSolution(int dist[], int bandwidth[], int delay[], double reliability[]) {
    printf("Vertex Distance     Bandwidth           Delay      Reliability\n");
    for (int i = 0; i < V; i++) {
        printf("%d \t %d \t\t %d \t\t %d \t\t %.2f", i, dist[i], bandwidth[i], delay[i], reliability[i]);
        printf("\n");
    }
}

void dijkstra(int graph[V][V], int bandwidth[V][V], int delay[V][V], double reliability[V][V], int src) {
    int dist[V];
    int bw[V];
    int delayArr[V];  
    int delayThreshold = 40;
    bool sptSet[V];
    double reliabilityy[V];
    double reliabilityThreshold = 0.70;

    for (int i = 0; i < V; i++) {
        dist[i] = INT_MAX;
        bw[i] = 0;
        delayArr[i] = 0;
        sptSet[i] = false;
        reliabilityy[i]= 0;
    }

    dist[src] = 0;
    bw[src] = 5;

    for (int count = 0; count < V - 1; count++) {
        int u = minDistance(dist, sptSet);
        sptSet[u] = true;

        for (int v = 0; v < V; v++) {
            if (!sptSet[v] && graph[u][v] && dist[u] != INT_MAX &&
                dist[u] + graph[u][v] < dist[v] && delayArr[u] + delay[u][v] < delayThreshold ) {
                
                dist[v] = dist[u] + graph[u][v];
                bw[v] = bandwidth[u][v];
                delayArr[v] = delayArr[u] + delay[u][v];
                reliabilityy[v] = reliabilityy[u] + reliability[u][v];
            }
        }
    }

    printSolution(dist, bw, delayArr, reliabilityy);
}

